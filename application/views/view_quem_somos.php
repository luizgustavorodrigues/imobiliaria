				<p>
					Uma das primeiras imobiliárias de Curitiba, iniciou suas atividades em 1959 com venda de lotes de terreno da Companhia Territorial Boqueirão. Nos anos 60, com a regulamentação da profissão do corretor de imóveis, a empresa se preparou para estender suas atividades passando a atuar com venda de imóveis, locação e administração de imóveis e posteriormente, loteamentos, o que ganhou um certo destaque no desenvolvimento de Curitiba. <br>

           
Um grande e muito conhecido empreendimento que a Imobiliária 2000 fez parte, foi o Jardim Schaffer, onde fica localizado o Bosque Alemão, que hoje se tornou sinônimo de morar com estilo devido às residências construídas. Outro feito bastante conhecido foi a concepção do Parque Tanguá, em que, para a comemoração dos 300 anos de Curitiba, a Imobiliária 2000, em parceria com a Prefeitura de Curitiba e o IPPUC, negociou parte dos terrenos da família Gava para a construção do parque, tendo a cidade ganho com uma nova opção de lazer e os proprietários ao redor que tem seu imóvel com linda vista para parque. <br>

           
Nos anos que se seguiram a Imobiliária 2000 construiu uma sólida reputação de empresa idônea, com grande credibilidade e de vanguarda por ter sido a primeira empresa a utilizar computadores para desenvolver atividades de administração imobiliária. Atualmente, seu sistema, desenvolvido por equipe própria, evoluiu proporcionando uma gestão de imóveis otimizada e com informação de qualidade disponível para proprietários, criando um diferencial em nosso atendimento. <br>

           
Hoje a Imobiliária 2000 atua com locação e administração de imóveis e incorporação de loteamentos e condomínios fechados em Curitiba e Região Metropolitana. A forma de atendimento é diferenciada, pois atua no conceito de Gerências de Contas o que proporciona um contato estreito entre empresa e cliente facilitando a gestão e oferecendo agilidade no atendimento. <br>

           
Outro fator que traz muitos benefícios aos clientes é que a Imobiliária 2000 faz parte da Rede Imóveis, uma associação de imobiliárias com o objetivo de realizar mais negócios. Com isso os corretores ganham outras possibilidades de maneira que o cliente tenha sempre as suas necessidades atendidas. <br>

           
Além da tradição, do pioneirismo e da história construída junto com Curitiba, a Imobiliária 2000 tem, acima de tudo, um compromisso com seus clientes. Prestar um atendimento de qualidade, proporcionar informações de interesse e principalmente atender as necessidades de seus clientes deixando-os sempre satisfeitos. <br>
